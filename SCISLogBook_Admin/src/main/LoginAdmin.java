 private Boolean validateAccount(String id, String password) throws SQLException {
        String status = null;
        Connection con;
        PreparedStatement ps;
        boolean condition = false;
        con = jdbc.connection.DBConnection.connectDB();
        String query = "SELECT admin_uname,admin_password FROM admin where admin_uname = ? AND admin_password = aes_encrypt(?,'scis2018'); ";
        ps = con.prepareStatement(query);

        ps.setString(1, id);
        ps.setString(2, password);

        ResultSet rs = ps.executeQuery();

        if (rs.next() ) {
            condition = true;
        }

        rs.close();
        ps.close();
        con.close();

        return condition;
    }
