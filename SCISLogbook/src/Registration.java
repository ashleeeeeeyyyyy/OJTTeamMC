    private Boolean idNumberExists(String idnumber) {
        boolean res = false;

        Connection con;
        Statement stmt;
        ResultSet rs;
        String query;

        try {
            con = jdbc.connection.DBConnection.connectDB();
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,
                    ResultSet.CONCUR_UPDATABLE);
            query = "SELECT idnumber from students where idnumber = " + idnumber;

            rs = stmt.executeQuery(query);
            if (rs.next()) {
                res = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        return res;
    }

    private Boolean codeValidator(String code) {
        boolean flag = false;

        if (code.length() == 4 && code.charAt(0) == '9') {
            return true;
        }

        return flag;
    }

    private void resetFields() {
        IDTextField.setText("");
        subjectComboBox.setSelectedIndex(-1);
        codeTextField.setText("");
        adviserComboBox.setSelectedIndex(-1);
    }

    private Boolean isFilledOut() {
        boolean res = true;

        if (IDTextField.getText().equals("") || adviserComboBox.getSelectedItem().toString().equals("")
                || codeTextField.getText().equals("") || subjectComboBox.getSelectedItem().toString().equals("")) {
            res = false;
        }

        return res;
    }

    private Boolean IDIsExisting(String id, String subject) {
        boolean res = false;
        Connection con;
        PreparedStatement ps;
        ResultSet rs;
        String query;

        try {
            con = jdbc.connection.DBConnection.connectDB();

            query = "SELECT * from `accounts` where `idnumber` = ? and `subject` = ?";

            ps = con.prepareStatement(query);
            ps.setString(1, id);
            ps.setString(2, subject);

            rs = ps.executeQuery();
            if (rs.next()) {
                res = true;
            }

            rs.close();
            ps.close();
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        return res;
    }

    public String[] faculty() throws SQLException {
        Connection con;
        con = jdbc.connection.DBConnection.connectDB();
        Statement stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,
                ResultSet.CONCUR_UPDATABLE);

        String query = "SELECT fac_name from faculty";
        ResultSet rs = stmt.executeQuery(query);

        ArrayList list = new ArrayList();

        while (rs.next()) {
            list.add(rs.getString("fac_name"));
        }
        String[] fac = new String[list.size()];
        list.toArray(fac);

        return fac;
    }

