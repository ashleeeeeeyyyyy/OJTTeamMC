    private Boolean validateAccount(String id, String password) throws SQLException {
        boolean res = false;
        Connection con;
        PreparedStatement ps;

        con = jdbc.connection.DBConnection.connectDB();
        String query = "SELECT idnumber, recovery_pass from students where idnumber = ? and "
                + "recovery_pass = aes_encrypt(?,'scis2018')";
        ps = con.prepareStatement(query);

        ps.setString(1, id);
        ps.setString(2, password);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            res = true;
        }

        rs.close();
        ps.close();
        con.close();

        return res;
    }
    
