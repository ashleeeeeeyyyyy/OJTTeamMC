    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        if (!Validator.idNumberExists(IDTextField.getText())) {
            JOptionPane.showMessageDialog(this, "ID Number is not registered.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!NewPassField.getText().equals(ConfNewPassField.getText())) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!Validator.passwordValidator(NewPassField.getText())) {
            JOptionPane.showMessageDialog(this, "Password should contain\n"
                        + "1. At least 8 characters.\n"
                        + "2. At least 1 uppercase and 1 lowercase\n"
                        + "    character.\n"
                        + "3. At least 1 numeric character.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection con = jdbc.connection.DBConnection.connectDB();
            String query = "UPDATE students SET password = aes_encrypt(?, 'scis2018') where idnumber = ?";
            try {
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, NewPassField.getText());
                ps.setString(2, IDTextField.getText());
                ps.executeUpdate();
                resetFields();
                JOptionPane.showMessageDialog(this, "Registered Successfully");
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(ResetPassword.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_resetButtonActionPerformed

